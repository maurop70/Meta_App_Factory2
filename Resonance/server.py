@app.post("/api/parent/profile")
def update_student_profile(req: StudentProfileRequest):
    """Saves the structured Guided Interview data to parent_config.json."""
    config = _load_parent_config()
    profile = config.get("student_profile", {})

    # Sanitize and merge provided fields
    VALID_SOCIAL_LEVELS = ["isolated", "small_group", "social", "leader", ""]
    VALID_SEVERITIES = ["low", "medium", "high"]
    VALID_STRESSORS = ["Academic Pressure", "Social Anxiety", "Test Anxiety", "Homework Overload", "Time Management", "Peer Conflicts"]
    VALID_LEARNING_STYLES = ["Visual", "Auditory", "Kinesthetic", "Reading/Writing"]

    if req.hobbies_interests is not None:
        # Allow custom hobbies, but sanitize string length
        profile["hobbies_interests"] = [h.strip()[:50] for h in req.hobbies_interests if h.strip()][:15]
    if req.social_level is not None:
        if req.social_level in VALID_SOCIAL_LEVELS:
            profile["social_level"] = req.social_level
        else:
            raise HTTPException(status_code=400, detail=f"Invalid social_level. Must be one of: {VALID_SOCIAL_LEVELS}")
    if req.academic_weak_areas is not None:
        sanitized_areas = []
        for area in req.academic_weak_areas[:10]:  # Max 10 areas
            if area.severity not in VALID_SEVERITIES:
                raise HTTPException(status_code=400, detail=f"Invalid severity '{area.severity}'. Must be one of: {VALID_SEVERITIES}")
            sanitized_areas.append({
                "subject": area.subject.strip()[:50],
                "specific_area": area.specific_area.strip()[:100],
                "severity": area.severity,
            })
        profile["academic_weak_areas"] = sanitized_areas
    if req.stress_indicators is not None:
        profile["stress_indicators"] = [s for s in req.stress_indicators if s in VALID_STRESSORS]
    if req.learning_style_preferences is not None:
        profile["learning_style_preferences"] = [l for l in req.learning_style_preferences if l in VALID_LEARNING_STYLES]

    config["student_profile"] = profile
    _save_parent_config(config)
    return JSONResponse({"status": "ok", "student_profile": profile})

