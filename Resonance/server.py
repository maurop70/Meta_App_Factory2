async def _extract_text_from_file(file_path: str, file_type: str) -> str:
    """Extracts text content from various file types."""
    extracted_text = ""
    try:
        if file_type in ["txt", "md", "csv"]:
            with open(file_path, "r", encoding="utf-8") as f:
                extracted_text = f.read()
        elif file_type == "pdf":
            if PyPDF2:
                with open(file_path, "rb") as f:
                    reader = PyPDF2.PdfReader(f)
                    for page in reader.pages:
                        extracted_text += page.extract_text() + "\n"
            else:
                extracted_text = "[PDF text extraction requires PyPDF2 library]"
        elif file_type == "docx":
            if Document:
                document = Document(file_path)
                for para in document.paragraphs:
                    extracted_text += para.text + "\n"
            else:
                extracted_text = "[DOCX text extraction requires python-docx library]"
        elif file_type in ["png", "jpg", "jpeg"]:
            extracted_text = f"[Image uploaded: {os.path.basename(file_path)}]"
        else:
            extracted_text = f"[Unsupported file type for text extraction: {file_type}]"
    except Exception as e:
        logger.error(f"Error extracting text from {file_path}: {e}")
        extracted_text = f"[Error extracting text: {e}]"
    return extracted_text

