@app.post("/api/upload")
async def upload_file(file: UploadFile = File(...)):
    """
    Accepts multipart file uploads, saves them, extracts text, and updates file context.
    Supported types: .txt, .pdf, .docx, .csv, .md, .png, .jpg, .jpeg
    """
    allowed_extensions = {".txt", ".pdf", ".docx", ".csv", ".md", ".png", ".jpg", ".jpeg"}
    file_extension = os.path.splitext(file.filename)[1].lower()

    if file_extension not in allowed_extensions:
        raise HTTPException(status_code=400, detail=f"File type {file_extension} not allowed.")

    # Sanitize filename to prevent path traversal
    safe_filename = os.path.basename(file.filename)
    file_path = os.path.join(UPLOADS_DIR, safe_filename)

    # Save the file
    try:
        with open(file_path, "wb") as buffer:
            shutil.copyfileobj(file.file, buffer)
        logger.info(f"File saved: {file_path}")
    except Exception as e:
        logger.error(f"Could not save file {safe_filename}: {e}")
        raise HTTPException(status_code=500, detail=f"Could not save file: {e}")

    # Extract text
    extracted_text = await _extract_text_from_file(file_path, file_extension.lstrip('.'))
    logger.info(f"Text extracted from {safe_filename}, length: {len(extracted_text)}")

    # Update file context
    file_context = _load_file_context()
    new_entry = {
        "filename": safe_filename,
        "type": file_extension.lstrip('.'),
        "extracted_text": extracted_text,
        "uploaded_at": datetime.now().isoformat()
    }
    # Remove existing entry if filename is the same (overwrite)
    file_context["files"] = [f for f in file_context["files"] if f["filename"] != safe_filename]
    file_context["files"].append(new_entry)
    _save_file_context(file_context)

    return JSONResponse({
        "status": "ok",
        "filename": safe_filename,
        "text_length": len(extracted_text)
    })

@app.get("/api/uploads")
async def get_uploads():
    """Returns a list of currently uploaded files and their metadata."""
    file_context = _load_file_context()
    # Return only metadata, not full text, for listing
    return JSONResponse({"files": [{"filename": f["filename"], "type": f["type"], "uploaded_at": f["uploaded_at"], "text_length": len(f["extracted_text"])} for f in file_context["files"]]})

@app.delete("/api/uploads/{filename}")
async def delete_upload(filename: str):
    """Removes a specific uploaded file and its entry from file_context.json."""
    file_path = os.path.join(UPLOADS_DIR, os.path.basename(filename)) # Sanitize filename
    if not os.path.exists(file_path):
        raise HTTPException(status_code=404, detail="File not found.")

    try:
        os.remove(file_path)
        file_context = _load_file_context()
        file_context["files"] = [f for f in file_context["files"] if f["filename"] != filename]
        _save_file_context(file_context)
        logger.info(f"File deleted: {filename}")
        return JSONResponse({"status": "ok", "message": f"File {filename} deleted."})
    except Exception as e:
        logger.error(f"Could not delete file {filename}: {e}")
        raise HTTPException(status_code=500, detail=f"Could not delete file: {e}")

